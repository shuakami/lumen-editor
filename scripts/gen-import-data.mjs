#!/usr/bin/env node
/**
 * Generates src/editor/import-data.ts from real toolchains, mirroring the data
 * sources VS Code's language servers enumerate:
 *  - Node builtins:   module.builtinModules (what tsserver offers for bare imports)
 *  - Python stdlib:   sys.stdlib_module_names (what Pylance indexes)
 *  - Go stdlib:       `go list std` (what gopls indexes)
 *  - C/C++ headers:   the compiler's real `#include <...>` search directories
 *                     (what cpptools scans), via `gcc -E -v`
 *  - Ruby stdlib:     the interpreter's rubylibdir require targets
 *
 * Run on a machine with node, python3, go, gcc/g++ and ruby installed:
 *   node scripts/gen-import-data.mjs
 */
import { execSync } from "node:child_process";
import { readdirSync, statSync, writeFileSync } from "node:fs";
import { builtinModules } from "node:module";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const run = (cmd) => execSync(cmd, { encoding: "utf8", maxBuffer: 64 * 1024 * 1024 });

// ---- Node builtins (tsserver: JsTyping.nodeCoreModules) ----
const nodeBuiltins = builtinModules
  .filter((m) => !m.startsWith("_"))
  .sort();

// ---- Python stdlib (Pylance: sys.stdlib_module_names) ----
const pythonStdlib = JSON.parse(
  run(
    `python3 -c "import sys, json; print(json.dumps(sorted(m for m in sys.stdlib_module_names if not m.startswith('_'))))"`,
  ),
);

// ---- Go stdlib (gopls: go list std) ----
const goStd = run("go list std")
  .split("\n")
  .map((s) => s.trim())
  .filter((s) => s && !s.startsWith("internal/") && !s.includes("/internal"))
  .sort();

// ---- C / C++ include directories (cpptools: compiler search paths) ----
function includeDirs(compiler, lang) {
  const out = execSync(
    `echo | ${compiler} -x${lang} -E -v - 2>&1 1>/dev/null || true`,
    { encoding: "utf8", shell: "/bin/bash" },
  );
  const lines = out.split("\n");
  const start = lines.findIndex((l) => l.includes("#include <...> search starts here"));
  const end = lines.findIndex((l) => l.includes("End of search list"));
  if (start < 0 || end < 0) throw new Error(`no search dirs from ${compiler}`);
  return lines.slice(start + 1, end).map((l) => l.trim()).filter(Boolean);
}

function scanHeaders(dirs, { allowNoExt = false, maxDepth = 2 } = {}) {
  const found = new Set();
  const walk = (root, rel, depth) => {
    let entries;
    try {
      entries = readdirSync(join(root, rel));
    } catch {
      return;
    }
    for (const name of entries) {
      const relPath = rel ? `${rel}/${name}` : name;
      const full = join(root, relPath);
      let st;
      try {
        st = statSync(full);
      } catch {
        continue;
      }
      if (st.isDirectory()) {
        if (depth < maxDepth) walk(root, relPath, depth + 1);
      } else if (
        name.endsWith(".h") ||
        name.endsWith(".hpp") ||
        (allowNoExt && !name.includes("."))
      ) {
        found.add(relPath);
      }
    }
  };
  for (const dir of dirs) walk(dir, "", 0);
  return [...found].sort();
}

/** Implementation-detail subtrees the compiler owns; not meant for direct inclusion. */
const INTERNAL_HEADER_DIRS = /^(bits|gnu|ssp|sanitizer|finclude|openacc|acc)\//;

const cDirs = includeDirs("gcc", "c");
const cppDirs = includeDirs("g++", "c++");
const cHeaders = scanHeaders(cDirs).filter((h) => !INTERNAL_HEADER_DIRS.test(h));
// C++ standard headers are extension-less files inside .../c++/<ver>
const cppOnlyDirs = cppDirs.filter((d) => d.includes("/c++/"));
const cppStdHeaders = scanHeaders(cppOnlyDirs, { allowNoExt: true, maxDepth: 1 }).filter(
  (h) => !h.includes("/") || h.startsWith("bits/") === false,
);
const cppHeaders = [...new Set([...cppStdHeaders.filter((h) => !h.includes("/")), ...cHeaders])].sort();

// ---- Ruby stdlib (require targets in rubylibdir) ----
const rubyLibDir = run(`ruby -e "print RbConfig::CONFIG['rubylibdir']"`).trim();
const rubyStdlib = [
  ...new Set(
    readdirSync(rubyLibDir)
      .filter((f) => f.endsWith(".rb"))
      .map((f) => f.slice(0, -3))
      .concat(
        readdirSync(rubyLibDir)
          .filter((f) => {
            try {
              return statSync(join(rubyLibDir, f)).isDirectory();
            } catch {
              return false;
            }
          })
          .flatMap((d) => {
            try {
              return readdirSync(join(rubyLibDir, d))
                .filter((f) => f.endsWith(".rb"))
                .map((f) => `${d}/${f.slice(0, -3)}`);
            } catch {
              return [];
            }
          }),
      ),
  ),
].sort();

const versions = {
  node: process.version,
  python: run("python3 -V").trim(),
  go: run("go version").trim().split(" ")[2],
  gcc: run("gcc -dumpfullversion").trim(),
  ruby: run(`ruby -e "print RUBY_VERSION"`).trim(),
};

const banner = `// Generated by scripts/gen-import-data.mjs — do not edit by hand.
// Sources: node ${versions.node}, ${versions.python}, go ${versions.go}, gcc ${versions.gcc}, ruby ${versions.ruby}
`;

const emit = (name, arr) =>
  `export const ${name}: readonly string[] = ${JSON.stringify(arr, null, 0)};\n`;

const outPath = join(dirname(fileURLToPath(import.meta.url)), "..", "src", "editor", "import-data.ts");
writeFileSync(
  outPath,
  banner +
    emit("NODE_BUILTINS", nodeBuiltins) +
    emit("PYTHON_STDLIB", pythonStdlib) +
    emit("GO_STDLIB", goStd) +
    emit("C_HEADERS", cHeaders) +
    emit("CPP_HEADERS", cppHeaders) +
    emit("RUBY_STDLIB", rubyStdlib),
);
console.log("written", outPath, {
  node: nodeBuiltins.length,
  python: pythonStdlib.length,
  go: goStd.length,
  c: cHeaders.length,
  cpp: cppHeaders.length,
  ruby: rubyStdlib.length,
});
